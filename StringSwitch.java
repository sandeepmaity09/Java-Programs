//Use a string to control a switch statement
class StringSwitch {
public static void main(String[] args) {
String str="two";
switch(str){
case "one":
System.out.println("One");
break;
case "two":
System.out.println("Two");
break;
case "three":
System.out.println("Three");
default:
System.out.println("Greater Then Three");
}
}
}
